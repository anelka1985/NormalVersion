package com.egame.beans;

import org.json.JSONObject;

/**
 * @author LiuHan
 *
 */
public class HobbyBean {
	private String interest;
    private String interestId;
    private String interestType;

	/**
	 * 
	 */
	public HobbyBean(JSONObject jobj) {
		this.interest = jobj.optString("interest", "");
		this.interestId = jobj.optString("interestId", "");
		this.interestType = jobj.optString("interestType", "");
	}

	/**
	 * @return the interest
	 */
	public String getInterest() {
		return interest;
	}

	/**
	 * @param interest the interest to set
	 */
	public void setInterest(String interest) {
		this.interest = interest;
	}

	/**
	 * @return the interestId
	 */
	public String getInterestId() {
		return interestId;
	}

	/**
	 * @param interestId the interestId to set
	 */
	public void setInterestId(String interestId) {
		this.interestId = interestId;
	}

	/**
	 * @return the interestType
	 */
	public String getInterestType() {
		return interestType;
	}

	/**
	 * @param interestType the interestType to set
	 */
	public void setInterestType(String interestType) {
		this.interestType = interestType;
	}

}
