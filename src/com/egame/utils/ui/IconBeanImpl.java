package com.egame.utils.ui;

import android.graphics.drawable.BitmapDrawable;

/**
 * 
 * @ClassName: IconBean
 * @Description: 带有icon的实体继承这个类
 * @author zhouzhe@lenovo-cw.com
 * @date 2011-7-29 上午12:06:55
 */
public class IconBeanImpl implements IconBean{
	
	BitmapDrawable icon;
	
	String iconurl;

	public IconBeanImpl(String iconurl) {
		this.iconurl = iconurl;
	}

	public IconBeanImpl() {

	}

	/**
	 * 
	 * @return 返回图标drawable
	 */
	public BitmapDrawable getIcon() {
		return icon;
	}

	/**
	 * 设置图标
	 * 
	 * @param icon
	 *            所设置的图标
	 */
	public void setIcon(BitmapDrawable icon) {
		this.icon = icon;
	}

	/**
	 * @return
	 */
	public String getIconurl() {
		return iconurl;
	}

	/**
	 * @param iconurl
	 */
	public void setIconurl(String iconurl) {
		this.iconurl = iconurl;
	}
}
